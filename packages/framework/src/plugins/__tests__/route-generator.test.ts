// Client routes file (dist/client/routes.generated.ts)
// ---------------------------------------------------------------------------

describe('generateClientRoutes — @vaadin/router Route[] format', () => {
  const routes = buildRoutes([
    'index.ts',
    'about.ts',
    'blog/index.ts',
    'blog/[slug].ts',
    '[...all].ts',
  ]);
  const source = generateClientRoutes(routes, ROOT_DIR);

  it('imports Route type from @vaadin/router', () => {
    expect(source).toContain("import type { Route } from '@vaadin/router'");
  });

  it('exports a `routes` array typed as Route[]', () => {
    expect(source).toContain('export const routes: Route[] = [');
  });

  it('contains an async action() callback for each route', () => {
    const actionCount = (source.match(/action: async \(\) => \{ await import\(/g) ?? []).length;
    expect(actionCount).toBe(5);
  });

  it('includes a `component` field for each route', () => {
    const componentCount = (source.match(/component:/g) ?? []).length;
    expect(componentCount).toBe(5);
  });

  it('generates correct import paths with .js extension for each route', () => {
    // All import paths should end in .js (not .ts) since they target the
    // Vite-compiled client bundle, not TypeScript source
    const importMatches = source.match(/await import\("([^"]+)"\)/g) ?? [];
    expect(importMatches).toHaveLength(5);
    for (const match of importMatches) {
      expect(match).toMatch(/\.js"\)$/);
    }
  });

  it('generates import paths rooted at the project (start with /)', () => {
    const importMatches = source.match(/await import\("([^"]+)"\)/g) ?? [];
    for (const match of importMatches) {
      // Extract the path from: await import("/path/to/file.js")
      const path = match.replace('await import("', '').replace('")', '');
      expect(path.startsWith('/')).toBe(true);
    }
  });

  it('sets the correct component tag for each route', () => {
    expect(source).toContain('component: "page-home"');
    expect(source).toContain('component: "page-about"');
    expect(source).toContain('component: "page-blog"');
    expect(source).toContain('component: "page-blog-slug"');
    expect(source).toContain('component: "page-all"');
  });

  it('sets the correct path for each route', () => {
    expect(source).toContain('path: "/"');
    expect(source).toContain('path: "/about"');
    expect(source).toContain('path: "/blog"');
    expect(source).toContain('path: "/blog/:slug"');
    expect(source).toContain('path: "/:all(.*)*"');
  });

  it('includes the @generated header comment', () => {
    expect(source).toContain('// @generated by litro page scanner — do not edit');
  });
});

// ---------------------------------------------------------------------------
// Prerender routes — only non-dynamic routes
// ---------------------------------------------------------------------------

describe('prerender routes extraction', () => {
  /** Mirrors the filter in pages.ts that populates nitro.options.prerender.routes */
  function extractPrerenderRoutes(routes: LitroRoute[]): string[] {
    return routes
      .filter(r => !r.isDynamic && !r.isCatchAll)
      .map(r => r.path);
  }

  it('includes only static (non-dynamic, non-catch-all) routes', () => {
    const routes = buildRoutes([
      'index.ts',
      'about.ts',
      'blog/index.ts',
      'blog/[slug].ts',   // dynamic — excluded
      '[...all].ts',      // catch-all — excluded
    ]);

    const prerenderRoutes = extractPrerenderRoutes(routes);

    expect(prerenderRoutes).toContain('/');
    expect(prerenderRoutes).toContain('/about');
    expect(prerenderRoutes).toContain('/blog');
    expect(prerenderRoutes).not.toContain('/blog/:slug');
    expect(prerenderRoutes).not.toContain('/:all(.*)*');
  });

  it('returns an empty array when all pages are dynamic', () => {
    const routes = buildRoutes(['[id].ts', '[...all].ts']);
    const prerenderRoutes = extractPrerenderRoutes(routes);
    expect(prerenderRoutes).toHaveLength(0);
  });

  it('returns all routes when none are dynamic', () => {
    const routes = buildRoutes(['index.ts', 'about.ts', 'contact.ts']);
    const prerenderRoutes = extractPrerenderRoutes(routes);
    expect(prerenderRoutes).toHaveLength(3);
    expect(prerenderRoutes).toContain('/');
    expect(prerenderRoutes).toContain('/about');
    expect(prerenderRoutes).toContain('/contact');
  });

  it('excludes optional param routes (isDynamic === true)', () => {
    const routes = buildRoutes(['[[lang]]/index.ts']);
    const prerenderRoutes = extractPrerenderRoutes(routes);
    // Optional params produce isDynamic: true — excluded from prerender
    expect(prerenderRoutes).toHaveLength(0);
  });
});

// ---------------------------------------------------------------------------
// Route sort order in generated output
// ---------------------------------------------------------------------------

describe('generateManifestModule — sort order', () => {
  it('places static routes before dynamic routes in the generated JSON', () => {
    const routes = buildRoutes([
      '[slug].ts',         // dynamic
      'about.ts',         // static
      'index.ts',         // static
    ]);
    const source = generateManifestModule(routes);

    const indexOfAbout = source.indexOf('"path": "/about"');
    const indexOfSlug = source.indexOf('"path": "/:slug"');

    expect(indexOfAbout).toBeLessThan(indexOfSlug);
  });

  it('places dynamic routes before catch-all routes in the generated JSON', () => {
    const routes = buildRoutes([
      '[...all].ts',      // catch-all
      'blog/[slug].ts',   // dynamic
    ]);
    const source = generateManifestModule(routes);

    const indexOfSlug = source.indexOf('"path": "/blog/:slug"');
    const indexOfCatchAll = source.indexOf('"path": "/:all(.*)*"');

    expect(indexOfSlug).toBeLessThan(indexOfCatchAll);
  });
});
